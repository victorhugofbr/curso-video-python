"""
EXERCÍCIO 006: Dobro, Triplo, Raiz Quadrada

Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.
"""


num = int(input("Informe um número qualquer: "))

dobro = num * 2
triplo = num * 3
raiz = num ** 0.5


print(f"Você informou o número {num}, seu dobro é igual á {dobro},  seu triplo é {triplo} e sua raiz quadrada é "
      f"{raiz}")